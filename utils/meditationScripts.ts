export interface ScriptSegment {
  time: number;
  endTime?: number;
  text: string;
}

// 텍스트 배열을 받아서 시작 시간(0)부터 step초(간격) 단위로 Time 객체를 매핑해주는 유틸
function generateTimeline(texts: string[], step: number = 15): ScriptSegment[] {
  return texts.map((text, idx) => ({
    time: idx * step,
    endTime: (idx + 1) * step,
    text
  }));
}

