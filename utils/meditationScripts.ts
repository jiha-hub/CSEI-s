// 감정명(키워드) 또는 명상 방식 이름에 맞춘 리턴 헬퍼
export function getScriptForMeditation(name: string): ScriptSegment[] {
  if (name.includes('호흡')) return SCRIPTS_BREATH;
  if (name.includes('리소스')) return SCRIPTS_RESOURCE;
  if (name.includes('신체')) return SCRIPTS_BODY_SCAN;
  if (name.includes('희희')) return SCRIPTS_HEHE;
  if (name.includes('하단전')) return SCRIPTS_LOWER_DANTIAN;
  if (name.includes('중단전')) return SCRIPTS_MIDDLE_DANTIAN;
  if (name.includes('상단전')) return SCRIPTS_UPPER_DANTIAN;
  
  return SCRIPTS_BREATH; // 기본
}
